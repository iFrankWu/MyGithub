package com.shinetech.dress.tag;

import java.util.ArrayList;
import java.util.List;

public class TagConfig {
	private String line;
	private List<Tag> oTagList = new ArrayList<Tag>();
	private List<String> oTagCatalogList = new ArrayList<String>();
	private List<Tag> dTagList = new ArrayList<Tag>();

	public List<Tag> getOTagList() {
		return oTagList;
	}

	public void addOTag(Tag otag) {
		oTagList.add(otag);
	}

	public List<String> getOTagCatalogList() {
		return oTagCatalogList;
	}

	public void addOTagCatalog(String tagCatalog) {
		oTagCatalogList.add(tagCatalog);
	}

	public List<Tag> getDTagList() {
		return dTagList;
	}

	public void addDTag(Tag dTag) {
		dTagList.add(dTag);
	}

	public String getLine() {
		return line;
	}

	public void setLine(String line) {
		this.line = line;
	}
}
